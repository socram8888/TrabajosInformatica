    private void continueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonActionPerformed
        Object asig = asigList.getSelectedValue();
        if (asig == null) {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ninguna asignatura", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (usuario.estaAlumnoMatriculado(asig)) {
            JOptionPane.showMessageDialog(this, "Ya te encuentras registrado en la asignatura escogida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        synchronized (taskCierreMatriculacion.getLock()) {
            if (taskCierreMatriculacion.expirado()) {
                return;
            }

            dispose();
            SeleccionarSubgrupo frame = new SeleccionarSubgrupo(usuario, taskCierreMatriculacion, asig);
            frame.setVisible(true);
            taskCierreMatriculacion.setVentanaActual(frame);
        }
    }//GEN-LAST:event_continueButtonActionPerformed

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        synchronized (taskCierreMatriculacion.getLock()) {
            taskCierreMatriculacion.cancel();
        }

        dispose();
        new VistaAlumno(usuario).setVisible(true);
    }//GEN-LAST:event_backButtonActionPerformed
